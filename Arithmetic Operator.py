a,b=int(input('Enter a:')),int(input('Enter b:'))
p=input('Enter an arithmetic operator:')
print('a=',a,'b=',b,'operator=',p)
if p=='+':
    print('Sum=',a+b)
elif p=='-':
    print('Substraction=',a-b)
elif p=='*':
    print('Product=',a*b)
elif p=='**':
    print('Power=',a**b)
elif p=='/':
    print('Inter quotient=',a/b)
elif p=='//':
    print('Float quotient=',a//b)
elif p=='%':
    print('Reaminder=',a%b)
else:
    print('Invlaid operator')
                            













                                  
